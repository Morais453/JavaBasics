public class Variaveis {
    public static void main(String[] args) {
        //Variavel é um valor volátil que pode ser reatribuido diversas vezes ao longo do programa
        int idade; //Inicialização de variável com o tipo inteiro
        idade = 2; //Preenchendo var com um número inteiro qualquer
        //Lembrando a variável só pode ser preenchida a partir de sua inicialização e não antes

        System.out.println("Idade: " + idade); //Printa a idade formatando com a String "Idade: " + var


    }

}
